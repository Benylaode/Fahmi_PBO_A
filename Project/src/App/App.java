    private void showEditNama(String nama) {
        Produk produk = DbController.ProdukByNama(nama);    
        String namaBaru = tf_nama.getText();
        DbController.updateNama(produk.getId(), namaBaru);
    }

    private void showEditHarga(String nama) {
        Produk produk = DbController.ProdukByNama(nama);
        String hargaBaru = tf_harga.getText();
        Long hargaBaruLong = Long.valueOf(hargaBaru);
        DbController.updateHarga(produk.getId(), hargaBaruLong);
    }

    private void showEditJumlah(String nama) {
        Produk produk = DbController.ProdukByNama(nama);
        String jumlahBaru= tf_jumlah.getText();
        int jumlahBaruInt = Integer.parseInt(jumlahBaru);
        DbController.updateJumlah(produk.getId(), jumlahBaruInt);
    }

