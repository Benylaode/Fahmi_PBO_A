    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        String name = tf_nama.getText();
        try{
            Long harga = Long.valueOf(tf_harga.getText());
            int stok = Integer.parseInt(tf_jumlah.getText());
            
            if (DbController.Masukkan(name, harga, stok)) {
                JOptionPane.showMessageDialog(null, "berhasil menginput data");
            } else {
                JOptionPane.showMessageDialog(null, "gagal menginput data");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "gagal menginput data");
        }
        
        tf_nama.setText("");
        tf_harga.setText("");
        tf_jumlah.setText("");
        
    }//GEN-LAST:event_btn_tambahActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        String nama = tf_nama.getText();
        if(nama.isEmpty()){
            JOptionPane.showMessageDialog(null, "Masukkan nama terlebih dahulu");
        }
        else if(tf_harga.getText().isEmpty()){
            if(tf_jumlah.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Lalu Masukkan Harga dan Jumlah yang ingin di edit");
            }
            else{
                JOptionPane.showMessageDialog(null, "Lalu Masukkan Harga yang ingin di edit");
            }
        }
        else if (tf_jumlah.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Lalu Masukkan Jumlah yang ingin di edit");
        }
        else{
            try{
            showEditNama(nama);
            showEditHarga(nama);
            showEditJumlah(nama);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, "Input tidak valid");
            }
            
            tf_nama.setText("");
            tf_harga.setText("");
            tf_jumlah.setText("");
            
            
        }
        

    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        String nama = tf_nama.getText();
        if(nama.isEmpty()){
            JOptionPane.showMessageDialog(null, "Masukkan nama terlebih dahulu");
        }
        else if(tf_harga.getText().isEmpty()){
            if(tf_jumlah.getText().isEmpty()){
                 DbController.deleteData(nama);
            }
            else{
                JOptionPane.showMessageDialog(null, "tidak perlu masukkan jumlah");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "tidak perlu masukkan harga");
        }
        tf_nama.setText("");
        tf_harga.setText("");
        tf_jumlah.setText("");
    }//GEN-LAST:event_btn_deleteActionPerformed

